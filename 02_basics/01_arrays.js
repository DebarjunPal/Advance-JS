//Arrays

//Slice and Splice:

//Original Array:
const myArr = [0, 1, 2, 3, 4, 5]
console.log("Original Array :",myArr);
//Slice:
const myArr1 = myArr.slice(1,3)
console.log(myArr1);
console.log("Modified Array :",myArr);
//Splice:
const myArr2 = myArr.splice(1,3)
console.log(myArr2);
console.log("Modified Array :",myArr);